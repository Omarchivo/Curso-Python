#Bucle for
#Sirve para recorrer una lista y realizar una accion por cada elemento de la lista

'''
lista = [1,2,3,4]

for valor in lista:
    print(valor)
'''

#Bucle for para diccionarios
'''
diccionario = {
    'clave1' : 'valor1',
    'clave2' : 'valor2'
}

for clave in diccionario:
    print(clave)
    print(diccionario[clave])
'''

#Bucle for para las tuplas
'''
lista = [(1,2), (3,4)]
for valor1, valor2 in lista:
    print(valor1)
    print(valor2)
'''

################  Bucle WHILE  ################

valor = 4

while valor < 10:
    print(valor)
    valor += 1